package baekJoonTest;

import java.util.Scanner;

public class String_5_1 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int A = sc.nextInt();
		String[] SB = new String[A];
		String[] SBB;
		int i = 0;
		
		
		
		for(int i = 0; i < SB.length; i++) {
			SB[i] = sc.nextLine();
		}
		
		for(String b : SB) {
			SBB[i] = b;
			i++;
		}
		
		
	}

}
